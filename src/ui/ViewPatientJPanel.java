    private void populateTable() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0);
        for(Patient patient : listOfPatients.getListOfPatients()){
            Object[] row = new Object[9];
            
            row[0] = patient.getName();
            row[1] = patient.getPatientID();
            row[2] = patient.getAge();
            row[3] = patient.getHouseNumber();
            row[4] = patient.getCommunityName();
            row[5] = patient.getCityName();
            row[6] = vitalSigns.getBloodPressure();
            row[7] = vitalSigns.getBodyTemperature();
            row[8] = vitalSigns.getPulseRate();
            
            model.addRow(row);
        }
        
        
    }
