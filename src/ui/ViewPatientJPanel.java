    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblPatient.getSelectedRow();

        if(selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Patient Detail to Delete !!!");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        Patient selectedPatient = (Patient)model.getValueAt(selectedRowIndex, 0);
        
        listOfPatients.deletePatient(selectedPatient);
        JOptionPane.showMessageDialog(this, "Patient Detail Deleted !!!");
        
        populateTable();
        txtPatientNameView.setText("");
        txtPatientIDView.setText("");
        txtAgeView.setText("");
        
        txtHouseView.setText("");
        
        txtCommunityView.setText("");
        txtCityView.setText("");
        
        
        txtBPView.setText("");
        txtTempView.setText("");
        
        txtPulseView.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateVActionPerformed
        // TODO add your handling code here:
        int i = tblPatient.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tblPatient.getModel();
        Patient selectedPatient = (Patient)model.getValueAt(i, 0);
        VitalSigns selectedVitalSign = (VitalSigns)model.getValueAt(i, 0);
        
        selectedPatient.setName(txtPatientNameView.getText());
        selectedPatient.setPatientID(txtPatientIDView.getText());
        selectedPatient.setAge(Integer.parseInt(txtAgeView.getText()));
        
        selectedPatient.setHouseNumber(txtHouseView.getText());
        selectedPatient.setCommunityName(txtCommunityView.getText());
        selectedPatient.setCityName(txtCityView.getText());
        selectedVitalSign.setBloodPressure(Double.parseDouble(txtBPView.getText()));
        selectedVitalSign.setBodyTemperature(Double.parseDouble(txtTempView.getText()));
        selectedVitalSign.setPulseRate(Double.parseDouble(txtPulseView.getText()));
                 
        listOfPatients.updatePatientList(i, selectedPatient);
        JOptionPane.showMessageDialog(this, "Vitals Added Successfully !!!");
        if(i >= 0) {
            model.setValueAt(txtPatientNameView.getText(), i, 0);
            model.setValueAt(txtPatientIDView.getText(), i, 1);
            model.setValueAt(txtAgeView.getText(), i, 2);
            model.setValueAt(txtHouseView.getText(), i, 3);
            
            model.setValueAt(txtCommunityView.getText(), i, 4);
            model.setValueAt(txtCityView.getText(), i, 5);
            model.setValueAt(txtBPView.getText(), i, 6);
            model.setValueAt(txtTempView.getText(), i, 7);
            model.setValueAt(txtPulseView.getText(), i, 8);
            
        }
        else {
            JOptionPane.showMessageDialog(this, "Error Updating Field !!!");
        }
        populateTable();
        txtPatientNameView.setText("");
        txtPatientIDView.setText("");
        txtAgeView.setText("");
        
        txtHouseView.setText("");
        
        txtCommunityView.setText("");
        txtCityView.setText("");
        
        txtBPView.setText("");
        txtTempView.setText("");
        
        txtPulseView.setText("");
    }//GEN-LAST:event_btnUpdateVActionPerformed


